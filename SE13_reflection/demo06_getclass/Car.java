package com.javase.SE13_reflection.demo06_getclass;

public class Car {
    public String name = "hh";
    public int price = 1111;
    public String color = "111";

    @Override
    public String toString() {
        return "Car{" +
                "name='" + name + '\'' +
                ", price=" + price +
                ", color='" + color + '\'' +
                '}';
    }
}
